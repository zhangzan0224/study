/*
1. 输出100个hello world
*/
for (let i = 0; i <100 ; i++) {
    console.log('hello world');
}
/*
2. 输出100~200
*/
for (let i = 100; i <200 ; i++) {
    console.log(i);
}
/*
3. 创建一个包含1~100的数组
*/
const array = []
for (let i = 0; i <100 ; i++) {
    array.push(i);
}
/*
4. 定义一个数组，遍历输出它的每一项
*/
const arr = [1,2,3,4,5,6,7,]
for (let i of arr) {
    console.log(i);
}
